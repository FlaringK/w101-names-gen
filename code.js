const randomFromArray = items => items[Math.floor(Math.random()*items.length)]

const generateName = () => {
  const doMasc = document.getElementById("masc").checked
  let doFem = document.getElementById("fem").checked
  
  const genFirst = document.getElementById("fname").checked
  const genLast1 = document.getElementById("lname1").checked
  const genLast2 = document.getElementById("lname2").checked

  if (genFirst) {
    const names = nonnames.concat((doFem ? femnames : []), (doMasc ? mascnames : []))
    document.getElementById("first").innerText = randomFromArray(names)
  }

  if (genLast1) document.getElementById("last1").innerText = randomFromArray(lnames1)
  if (genLast2) document.getElementById("last2").innerText = randomFromArray(lnames2)
}

// Add options to Selectors

const addOptionToSelect = (text, selectId) => {
  let option = document.createElement("option")
  option.innerText = text
  option.value = text
  document.getElementById(selectId).appendChild(option)
}

lnames1.forEach(e => addOptionToSelect(e, "selectlname1"))
lnames2.forEach(e => addOptionToSelect(e, "selectlname2"))

nonnames.forEach(e => addOptionToSelect(e, "andgroup"))
femnames.forEach(e => addOptionToSelect(e, "femgroup"))
mascnames.forEach(e => addOptionToSelect(e, "mascgroup"))

let updateFullName = () => {
  document.getElementById("first").innerText = document.getElementById("selectfname").value
  document.getElementById("last1").innerText = document.getElementById("selectlname1").value
  document.getElementById("last2").innerText = document.getElementById("selectlname2").value
}

